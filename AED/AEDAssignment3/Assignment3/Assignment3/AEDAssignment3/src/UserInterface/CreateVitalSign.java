    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
       
        LocalDateTime  lTime =  LocalDateTime.now();
        String sBP = txtBloodPressure.getText().trim();
        String sHR = txtHeartRate.getText().trim();
        String sRR = txtRespiratoryRate.getText().trim();
        String sW = txtWeight.getText().trim();
        try
        {
            int i ;
            i = (sBP.length()* sHR.length()* sRR.length()* sW.length());
            if ( i==0)
            {

                throw new Exception("Empty");

            }

            else
            {

                int BP = Integer.parseInt(sBP);

                int HR = Integer.parseInt(sHR);

                int RR =Integer.parseInt(sRR);

                double W = Double.parseDouble(sW);

                VitalSign vitalSign = person.getPatient().getVSH().createVitalSign();
                int age = person.getAge();
                String stat="";
               stat= vitalSign.statusD(stat,BP,HR,RR,W,age) ;
                    vitalSign.setBloodPressure(BP);
                    vitalSign.setHeartRate(HR);
                    vitalSign.setRespiratoryRate(RR);
                    vitalSign.setWeight(W);
                    vitalSign.setDate(lTime);
                    vitalSign.setStatus(stat);
                    JOptionPane.showMessageDialog(null,"Vital Sign saved successfully");

                    txtBloodPressure.setText("");
                    txtHeartRate.setText("");
                    txtRespiratoryRate.setText("");
                    txtWeight.setText("");
                   
                    txtDate.setText(String.valueOf(lTime));

                
            }
        }
        catch(Exception e)
        {
            if
            (e.toString().contains("Empty"))
            {
                JOptionPane.showMessageDialog(null,"Fields can not be empty", "Information Message",JOptionPane.INFORMATION_MESSAGE);
            }
            else
            {
                JOptionPane.showMessageDialog(null,"Format should be correct", "Information Message",JOptionPane.INFORMATION_MESSAGE);
            }

        }
        finally
        {

        }
    }//GEN-LAST:event_btnSaveActionPerformed


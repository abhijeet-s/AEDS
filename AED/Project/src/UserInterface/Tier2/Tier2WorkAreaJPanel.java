public void populateTable(){
    String allStatus = cbAll.getSelectedItem().toString();
    String myStatus = cbMy.getSelectedItem().toString();
        DefaultTableModel model = (DefaultTableModel)workRequestJTable.getModel();
        DefaultTableModel myModel = (DefaultTableModel)myWorkRequestJTable.getModel();
        model.setRowCount(0);
         myModel.setRowCount(0);
        for(WorkRequest request : tier2Organization.getWorkQueue().getWorkRequestList()){
            Object[] row = new Object[4];
            row[0] = request;
            row[1] = request.getSender().getPerson().getFirstName();
            
            row[2] =  ((TierHelpDesk) request).getThreatDetails().getPriority();
            row[3] =  ((TierHelpDesk) request).getThreatDetails().getSeverity();
            
            
         String reciever =   request.getReceiver() == null ? null : request.getReceiver().getPerson().getFirstName();
         if(userAccount.getPerson().getFirstName().equals(reciever))
           {
               if (myStatus == "All")
               {
                myModel.addRow(row);
               }
                else if(myStatus == "Close"  && request.equals(myStatus))
                        {
                        myModel.addRow(row);
                        }
           else if(myStatus == "Assigned" && request.equals(myStatus) )
                        {
                            myModel.addRow(row);
                        
                        }
               else if(myStatus == "InProgress" && request.equals(myStatus) )
                        {
                            myModel.addRow(row);
                        
                        }
           }
            else
           {
               if (allStatus == "All")
               {
                model.addRow(row);
               }
                else if(allStatus == "Close"  && request.getStatus().equals(allStatus))
                        {
                        model.addRow(row);
                        }
           else if(allStatus == "Open" && request.getStatus().equals(allStatus) )
                        {
                            model.addRow(row);
                        
                        }
              //  model.addRow(row);
           }
        }
    }

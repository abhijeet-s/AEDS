    private void assignJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_assignJButtonActionPerformed

        int selectedRow = workRequestJTable.getSelectedRow();

        if (selectedRow < 0){
            
           JOptionPane.showMessageDialog(null, "Please Select a row.");
           return ;
        }

        WorkRequest request = (WorkRequest)workRequestJTable.getValueAt(selectedRow, 0);
         if (request.getStatus().equals("Open")){
            request.setReceiver(userAccount);
        request.setStatus("Assigned");
        populateTable();
            return ;
        }
        JOptionPane.showMessageDialog(null, "Selected request is closed");
          
    }//GEN-LAST:event_assignJButtonActionPerformed

    private void processJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_processJButtonActionPerformed

        int selectedRow = myWorkRequestJTable.getSelectedRow();

        if (selectedRow < 0){
            JOptionPane.showMessageDialog(null, "Please Select a row.");
            return;
        }

        TierHelpDesk  request = (TierHelpDesk)myWorkRequestJTable.getValueAt(selectedRow, 0);
if (request.getStatus().equals("Assigned"))
{
        request.setStatus("InProgress");
}
if (request.getStatus().equals("InProgress"))
{
        
        ThreatProcess threatProcess = new ThreatProcess(userProcessContainer,request,2,enterprise);
        userProcessContainer.add("ThreatProcess2", threatProcess);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);
        return;
    }//GEN-LAST:event_processJButtonActionPerformed
else{    
JOptionPane.showMessageDialog(null, "Selected request is closed");
    }
    }
